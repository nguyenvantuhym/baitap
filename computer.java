class computer
{
	private String name;
	private String gen;
	private String nameRam;
	private String nameCPU;
	
	public void setName(String _name)
	{
		name =  _name;
	}
	public String getName()
	{
		return name;
	}
	
	public void setGen(String _gen)
	{
		gen =  _gen;
	}
	public String getGen()
	{
		return gen;
	}
	public void setNameRam(String _nameRam)
	{
		nameRam =  _nameRam;
	}
	public String getNameRam()
	{
		return nameRam;
	}
	public void setNameCPU(String _nameCPU)
	{
		nameCPU =  _nameCPU;
	}
	public String getNameCPU()
	{
		return nameCPU;
	}
	
}